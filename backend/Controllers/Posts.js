const postModel = require("../Models/posts");
const Like = require("../Models/Like");
const Comment = require("../Models/Comments");
const { uploadToCloudinary } = require("../utils/cloudinary");
const mongoose = require("mongoose");

const addPost = async (req, res) => {
  try {
    const { title, desc } = req.body;
    const user = req.user;

    if (!title || !desc) {
      return res.status(400).json({
        message: "Title and description are required",
        success: false,
      });
    }

    let url;
    if (req.file) {
      const localFilePath = req.file.path;
      const result = await uploadToCloudinary(localFilePath);
      if (!result) {
        return res.status(500).json({ error: "Upload failed" });
      }
      url = result.secure_url;
    }

    const post = new postModel({
      title,
      desc,
      image: url || null,
      userId: user._id,
      like: 0,
    });

    await post.save();
    const populatedPost = await post.populate("userId", "name email image");

    return res.status(200).json({
      message: "Post added successfully",
      success: true,
      post: populatedPost,
    });
  } catch (err) {
    return res.status(500).json({
      message: "Server not responding",
      success: false,
      error: err.message,
    });
  }
};

const fetchPosts = async (req, res) => {
  try {
    const limit = 20;

    const postsAgg = await postModel.aggregate([
      { $sample: { size: limit } },
      {
        $lookup: {
          from: "comments",
          localField: "_id",
          foreignField: "postId",
          as: "comments",
        },
      },
    ]);

    const posts = await postModel.populate(postsAgg, {
      path: "userId",
      select: "name email image",
    });

    return res.status(200).json({ success: true, posts });
  } catch (err) {
    return res.status(500).json({
      message: "Server not responding",
      success: false,
      error: err.message,
    });
  }
};

const addComment = async (req, res) => {
  try {
    const { postId, comment } = req.body;
    const user = req.user;

    if (!postId || !comment) {
      return res.status(400).json({
        message: "Post ID and comment are required",
        success: false,
      });
    }

    const post = await postModel.findById(postId);
    if (!post) {
      return res.status(404).json({
        message: "Post not found",
        success: false,
      });
    }

    await Comment.create({
      postId,
      userId: user._id,
      username: user.name,
      text: comment,
      pic: user.image,
    });

    const comments = await Comment.find({ postId }).sort({ createdAt: -1 });

    return res.status(200).json({
      message: "Comment added successfully",
      success: true,
      post: {
        ...post.toObject(),
        comments,
      },
    });
  } catch (err) {
    return res.status(500).json({
      message: "Server not responding",
      success: false,
      error: err.message,
    });
  }
};

const like = async (req, res) => {
  try {
    const userId = req.user._id;
    const { postId } = req.body;

    if (!postId) {
      return res
        .status(400)
        .json({ message: "Post Id is required", success: false });
    }

    const post = await postModel.findById(postId);
    if (!post) {
      return res
        .status(404)
        .json({ message: "Post not found", success: false });
    }

    const existingLike = await Like.findOne({ userId, postId });

    if (existingLike) {
      await existingLike.deleteOne();
      post.like = Math.max(post.like - 1, 0);
    } else {
      await Like.create({ userId, postId });
      post.like += 1;
    }

    await post.save();

    const updatedPost = await postModel
      .findById(postId)
      .populate("userId", "name email image");

    const comments = await Comment.find({ postId }).sort({ createdAt: -1 });

    return res.status(200).json({
      message: existingLike ? "Like removed" : "Like added successfully",
      success: true,
      updatedPost: {
        ...updatedPost.toObject(),
        comments,
      },
    });
  } catch (err) {
    return res.status(500).json({ message: "Server error", success: false });
  }
};

const fetchSinglePost = async (req, res) => {
  try {
    const { postId } = req.body;

    const post = await postModel
      .findById(postId)
      .populate("userId", "name email image");

    if (!post) {
      return res.status(404).json({
        message: "Post Not Found",
        status: false,
      });
    }

    const comments = await Comment.find({ postId }).sort({ createdAt: -1 });

    return res.status(200).json({
      message: "Post Fetched Successfully",
      status: true,
      post,
      comments,
    });
  } catch (error) {
    return res.status(500).json({
      message: "Something Went Wrong",
      status: false,
    });
  }
};

module.exports = {
  fetchSinglePost,
  addPost,
  fetchPosts,
  addComment,
  like,
};
